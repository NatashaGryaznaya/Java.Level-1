package ru.geekbrains.lesson7.lesson;

public class PlateFood {
    public int food;
    public PlateFood(int food) {
        this.food = food;
    }
    public void decreaseFood(int n) {
        food -= n;
    }
    public void increaseFood(int x) {
        food += x;
    }
    public void info() {
        System.out.println("Всего еды: " + food);
    }
}
